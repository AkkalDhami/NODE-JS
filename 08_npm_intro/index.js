//? Semantic Versioning System

//? Major, Minor, Patch

//? 1.0.0

//? last 0: patch -> small changes

//? Second last 0: minor -> minor changes

//? 1: major -> major changes, break changes

//? caret(^): minor and patch up to next minor allowed

//? tilde(~): ONLY patch are allowed

//? exclamation(!): major

//? exact: fixed version

//? x: wildcard for minor and patch